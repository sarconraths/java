public class Variablen {
	public static void main(String[] args){
		int swag;
		int yolo=5;
		swag=10;
		
		System.out.println(swag);
		System.out.println(yolo);
		
		double ergebnis;
		ergebnis = swag + yolo;
		System.out.print("Das Ergebnis lautet: ");
		System.out.println(ergebnis);
		ergebnis = swag - yolo;
		System.out.println(ergebnis);
		ergebnis = swag / yolo;
		System.out.println(ergebnis);
		ergebnis = swag * yolo;
		System.out.println(ergebnis);

		
	}
}

